"use client";

import { useState } from "react";

/**
 * 편집장(엘아울) 대기 멘트 풀.
 *
 * 같은 문구만 반복되면 LLM 응답 대기 중이라는 느낌이 강해지고 몰입이 깨진다는
 * 피드백을 받아, 매 마운트마다 풀에서 한 줄을 무작위로 골라 보여준다.
 *
 * 사용 규칙:
 * - 컴포넌트 최상단(다른 훅과 함께)에서 `const waitMsg = useEditorWait()` 로 받아
 *   `<NarrationBlock text={waitMsg} />` 같은 식으로 렌더.
 * - useState 초기값으로 한 번만 계산되므로, 로딩 도중 리렌더가 일어나도 멘트는
 *   바뀌지 않는다 (깜빡임 방지).
 * - 씬별로 분위기가 강하게 다른 곳(예: 매거진 최종 빌드)은 굳이 이 풀에 합치지
 *   말고 그 씬 고유의 멘트를 그대로 둘 것.
 */
const POOL: readonly string[] = [
  "편집장이 고심하는 중…",
  "편집장이 답변을 가만히 들여다본다…",
  "편집장이 펜을 잠시 멈춥니다…",
  "편집장이 페이지를 천천히 넘기고 있어요…",
  "편집장이 이야기의 결을 따라가는 중…",
  "편집장이 한 문장을 다시 매만지고 있어요…",
  "편집장이 안경을 고쳐 쓰며 들여다봅니다…",
  "편집장이 두 페이지를 나란히 펼쳐보고 있어요…",
  "편집장이 메모 사이를 오가고 있어요…",
  "편집장이 잠시 호흡을 고르는 중…",
  "편집장이 단어 하나를 만지작거리고 있어요…",
  "편집장이 책상 위 메모를 다시 정리해요…",
  "편집장이 행간을 따라 천천히 읽어내려가요…",
  "편집장이 잉크가 마르길 기다립니다…",
];

export function useEditorWait(): string {
  const [msg] = useState(() => POOL[Math.floor(Math.random() * POOL.length)]);
  return msg;
}
